x = 50
print(type(x)) #it will print INT type cuz x is 50 and 50 is integer

x = "Hello world"
print(type(x)) #it will print STR type cuz now x is string 

x = 23.4
print(type(x)) #now it will print FLOAt type cuz it has a point in the number

x = ["apple", "banana", "cherry"]
print(type(x)) #it will print LIST type cuz it has a several values in

x = ("apple", "banana", "cherry")
print(type(x)) #same with list but now we cant change values so its TUPLE

x = {"name" : "John", "age" : 36}
print(type(x)) # This {} braces means dictionary so it print DICT

x = True
print(type(x)) #its boolean type so answer is BOOL